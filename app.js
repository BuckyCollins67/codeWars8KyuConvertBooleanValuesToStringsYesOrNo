function boolToWord( bool ){
    if (bool === true){
      return('Yes')
    }else{
      return('No')
    }
  }
  
  //PREP
  
  //  P - Parameters
  //  Boolean true, or Boolean false
  
  //  R - Returns
  //  String "Yes" for true, String "No" for false
  
  //  E - Examples
  //  boolToWord(true) -> return 'yes'
  //  boolToWord(false) -> return 'no'
  
  //  P - Pseudocode
  //  If I get true, return 'yes', otherwise, return 'no'